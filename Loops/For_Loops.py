# This loop iterate over a sequense 

numbers = [10, 15, 20, 25]
# list of numbers
for number in numbers:
    # the for keyword iterate over numbers and assign each value to the control variable number
    print(number)



# Range function can create a lists that contains all numbers between two numbers
for x in range(100):
    print(x)

for y in range(10,50):
    print(y)
